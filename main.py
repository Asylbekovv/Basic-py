# min выводит минимальное значение
print("Result: ", min(3, 4, 6, 7, -1))
# max выводит максимальное значение
print("Result second: ", max(44, 33, 55, 56, 78, 100))
# pow возводит в степень
print("Result third: ", pow(23, 2))
# abs меняет местами по модулю
print("Result forth: ", abs(-12))
# round округление числа
print("Result five: ", round(14, 90))
